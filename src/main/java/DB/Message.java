package DB;

public class Message {
    int messageId;
    String message;
    int isPicture;
    int fromWho;

    public int getMessageId() {
        return messageId;
    }

    public String getMessage() {
        return message;
    }

    public int getIsPicture() {
        return isPicture;
    }

    public int getFromWho() {
        return fromWho;
    }
}
